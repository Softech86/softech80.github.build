<h3 id="tjmooc-404-">TJMOOC · 404页面</h3>
<p>TJMOOC 是一个数学慕课网站，设计、前端都由我独立完成。404 页面对网站功能性没有影响，却是用户与网站交互过程中不可缺少的一环。</p>
<p>404 来源于 HTTP 请求中的 404 状态码，表示服务器无法找到文件。但在网络亚文化发展过程中，404 被从其他状态码中提升出来，成为一个单独的意向，并被赋予多重内涵。它可以代表无法找寻的迷茫，代表计划之外的错误；象征着某种迷思，甚至成为暗流涌动的政治隐喻。</p>
<p>沿着内涵放射思考，我们可以联想到类似意向如巨石阵、潘洛斯三角形，从中提取出抽象几何图形，通过平行排布与视觉错觉，表现错误、迷茫的感受。</p>
<p>页面字体选择无衬线字体，融入几何感，干净利落。左下角的话来自费马对费马大定理的证明：“我有一个对这个命题的十分美妙的证明，可惜空白太小，我写不下。”</p>
