<h2 id="-">学生会的海报</h2>
<p>大二时加入校学生会，机缘巧合进入负责宣传的部门。日常任务即包括活动海报、易拉宝、传单之类宣传品，每张宣传品主题应与活动契合。</p>
<h3 id="-">脱机阅读</h3>
<p>在这些原则的指导下，我为脱机阅读活动设计了易拉宝。脱机阅读活动旨在让同学们抽出一段时间，远离手机等等电子设备，贴近纸质书籍，以传统的方式阅读文章。</p>
<p>因为活动内容为阅读，我特意选用了书本、眼镜、墨迹、阅读者这几个意向；在字体的选取上，使用书法痕迹明显的楷书，并对引导线做书法笔画处理；在颜色的选取上，选用传统中国色中的墨色。由此，我希望观众在观看海报的过程中，通过这些意向唤醒儿时对书画的记忆，产生参与阅读活动的欲望。</p>
<p><img src="res/脱机阅读易拉宝.png" alt="脱机阅读易拉宝"></p>
<h3 id="-">暗恋桃花源</h3>
<p>本次活动为话剧《暗恋桃花源》的沙龙，需要海报用作宣传，吸引有兴趣的同学了解这个话剧，参与到沙龙的讨论中。</p>
<p>由于主题是一场话剧，海报的设计中应当适当突出传统元素。海报中运用两种不同的字体：海报文案和活动信息使用可读性较好的木刻宋体，海报标题使用极具张力的行书，增强整张海报的文化氛围。作为点缀的木刻元素和印章元素，也同字体的风格相互照应。</p>
<p>海报的背景化用了话剧的一个场景。这个场景中，桃花林与轮椅作为话剧中两个剧中剧的部分摆放在一起，体现了两部分剧情和精神的对立与融合。</p>
<p>设计海报时，需要加入三个公众号二维码和一个活动二维码；二维码的形状与主题毫无关联。这张海报中，公众号的二维码被放置于页面左下角舞台留白位置，预约二维码与活动信息作为整体浮动于话剧场景上方，在不使用异形二维码的情况下，这样的做法既将二维码合理安排，明显易扫，又不割裂画面和意向，从而兼顾信息展示和氛围渲染。</p>
<p><img src="res/暗恋桃花源海报.png" alt="暗恋桃花源海报"></p>
<p>现在看这张海报，印章、分割线以及半透明桃花的装饰痕迹过重，甚至有些喧宾夺主，反而影响了主题表达以及观众对信息的接收。</p>
<h3 id="-">人工智能</h3>
<p>这是给人工智能讲座设计的黑白传单。作为传单，传递的信息（活动信息及文案）应当首要清晰、一目了然。受限于打印设备和打印效果，整个海报应简洁、高对比。本次活动海报先于传单，字体和元素已给定，设计风格应保持统一。</p>
<p>将人工智能的缩写 <strong>AI</strong> 变形为几何图形，简练、具有未来感。相互连接的网络寓意机器学习中相互联结的神经元，以此作为背景，可以丰富画面内容，增添细节。</p>
