<h3 id="-">初探 · 搜索</h3>
<p>第一次做网站设计是在两年以前，当时学校某机构打算做一个搜索引擎。本来只是发呆，随手涂了一个 Logo 出来，忽然就有了灵感，干脆整个网站的页面设计都自己做。网站布局同谷歌无二，但有些细节值得一提。</p>
<p><img src="res/同济大学.png" alt="_这是同济大学校徽">
<img src="res/同济搜索手绘.jpg" alt="_这是我涂鸦的Logo"></p>
<p>网站为同济内部的搜索引擎。根据搜索引擎的功能，从搜索动作中抽离出放大镜的元素；在草图的基础上，从同济校徽中抽离出龙舟的元素。因为同济师生为网站主要用户，同济龙舟元素在 Logo 中的化用可以增强使用者的内心认同。</p>
