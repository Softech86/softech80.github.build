<p>在百度、谷歌等等搜索引擎的页面设计中，页码部分都有自己的元素，如百度用熊脚印表示分页页码，谷歌用 G<strong>oo</strong>gle 中多个不同颜色的字母 <strong>o</strong> 表示。既然如此，同济校徽中有三个并排划船的人，这恰好构成了分页的重要素材。只需将同济校徽稍作变形拆分，即可用来表示分页。</p>
<p><img src="res/同济搜索页码.png" alt="_划船小人组成的分页页码"></p>
